# for loop with 1 argument
print('My name is')
for i in range(5):
    print('Jimmy Five Times ' + str(i))

# for loop with 2 arguments
print('My name is')
for i in range(10, 15):
    print('Jimmy Five Times ' + str(i))

# for loop with 3 arguments
print('My name is')
for i in range(5, 0, -1):
    print('Jimmy Five Times ' + str(i))

#while loop
print('My name is')
i = 0
while i < 5:
    print('Jimmy Five Times ' + str(i))
    i = i + 1